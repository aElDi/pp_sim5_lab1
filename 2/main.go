package main

import (
	"fmt"
)

func main() {
	var myInt int = 42
	var myFloat float64 = 3.14159
	var myString string = "Привет, мир"
	var myBool bool = true

	fmt.Println("Целочисленная переменная (int):", myInt)
	fmt.Println("Переменная с плавающей запятой (float64):", myFloat)
	fmt.Println("Строковая переменная (string):", myString)
	fmt.Println("Логическая переменная (bool):", myBool)
}